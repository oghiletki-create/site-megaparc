const applyLang = lang => {
  const dict = I18N[lang] || I18N.ro;
  document.documentElement.lang = lang;
  document.title = dict['meta.title'];
  document.getElementById('meta-desc').setAttribute('content', dict['meta.desc']);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const value = dict[el.dataset.i18n];
    if (value !== undefined) el.innerHTML = value;
  });
  document.querySelectorAll('.lang button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  try { localStorage.setItem('megaparc-lang', lang); } catch (e) {}
};

const savedLang = (() => {
  try { return localStorage.getItem('megaparc-lang'); } catch (e) { return null; }
})();

applyLang(savedLang && I18N[savedLang] ? savedLang : 'ro');

document.querySelectorAll('.lang button').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});
